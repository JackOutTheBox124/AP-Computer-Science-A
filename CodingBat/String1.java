package CodingBat;

public class String1 {
  /**
   * Given a string name, e.g. "Bob", return a greeting of the form "Hello Bob!".
   * <br>
   * helloName("Bob") → "Hello Bob!"
   * helloName("Alice") → "Hello Alice!"
   * helloName("X") → "Hello X!"
   */
  public String helloName(String name) {
    return "Hello " + name + "!";
  }
}
